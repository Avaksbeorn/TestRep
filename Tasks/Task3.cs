/*1.	Номера автобусов. Однажды Вася очень долго просидел на остановке, прежде чем дождался своего автобуса. 
 * Чтобы как-то занять время, он решил записывать на листочке государственные регистрационные номера проходящих мимо автобусов. 
 * При этом производилась запись лишь основного номера, без учета региональной принадлежности. 
 * Основная часть государственного регистрационного номера состоит из 6 символов: трех букв и трех цифр. 
 * Сначала идет буква, затем 3 цифры и еще 2 буквы заканчивают запись. В качестве цифр могут использоваться любые цифры от 0 до 9, 
 * а в качестве букв только прописные буквы, обозначения которых присутствуют как в английском, так и в русском алфавите, 
 * т.е. только следующие символы: A, B, C, E, H, K, M, O, P, T, X, Y. Например, «P204BT» - правильный номер, а «X182YZ» и «ABC216» - нет. 
 * Ваша задача заключается в проверке правильности проделанной Васей работы. А именно, нужно определить, какие из номеров соответствуют принятому стандарту, а какие нет. 
 * Номера читаем в цикле из консоли. Если номер пустой (нажали ENTER), то выходим из программы.
 */

using System.Text.RegularExpressions;

namespace ConsoleApp1.Tasks
{
    public static class Task3
    {
        public static void Run() {
            string pattern = @"^[ABCEHKMOPTXY]\d{3}[ABCEHKMOPTXY]{2}$";
            Regex regex = new Regex(pattern);

            while (true) {
                Console.WriteLine("Введите номер автобуса (или нажмите ENTER для выхода):");
                string input = Console.ReadLine();

                if (string.IsNullOrEmpty(input)) {
                    break;
                }

                if (regex.IsMatch(input)) {
                    Console.WriteLine("Номер корректен.");
                }
                else {
                    Console.WriteLine("Номер некорректен.");
                }
            }
        }
    }
}
